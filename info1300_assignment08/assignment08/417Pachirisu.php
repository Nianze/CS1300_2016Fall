        <a class="anchor" id="Introduction"></a>
        <h2>Introduction</h2>
 	    <p>Pachirisu (Japanese: パチリス Pachirisu) is an Electric-type Pokémon.</p> 
 	    <p>It is not known to evolve into or from any other Pokémon.</p>
        
        <a class="anchor" id="Biology"></a>        
        <h2>Biology</h2>
        <p>Pachirisu is a small, white squirrel-like Pokémon with a faint blueish tint and a light blue stripe starting from its forehead and goes all the way down to the tip of the tail. A female Pachirisu's blue stripe is shorter than a male's. A large white tooth grows out of its mouth. It has deep gray eyes and a yellow circle on each cheek. Its tail is almost twice as big as its body, with three spikes at the top of the curl, which it can use to shoot charges. Though its arms and legs are very short, it can run incredibly fast, enabling it to tire pursuing Pokémon out quickly. It has a great amount of electricity flowing in its body. If it builds up enough charge, it may shock other Pokémon.</p>
        <p>When it sleeps, Pachirisu likes to curl its big, fluffy tail round to the front of its body and uses it as a pillow. It also likes to chase and groom its tail. When a Pachirisu sheds its fur it shapes them into furballs, which have static charges just like its fur. It hides its favorite foods—which mainly consist of berries—in these static puffs and stores them in tree holes. It does this to protect the food; if a human or another Pokémon tries to take the food, they will get shocked from the static. It may rub cheek pouches with another Pachirisu to share stored electricity. It is generally found in tree tops in forests, as well as the outskirts of a forest and near power plants.</p>
    
