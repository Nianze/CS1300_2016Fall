        <a class="anchor" id="Origin"></a>
        <h2>Origin</h2>
        <p>Pachirisu is based on a squirrel. The spikes on its tail (and possibly its ears) could be a reference to static electricity's impact on hair.</p>

        <a class="anchor" id="NameOrigin"></a>
        <h2>Name Origin</h2>
        <p>Pachirisu is a combination of パチパチ pachipachi (electric crackling) and 栗鼠 risu (squirrel).</p>
